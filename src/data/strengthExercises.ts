// Function to find exercise info by name (with fuzzy matching)
export function findExercise(name: string): ExerciseInfo | null {
  // Direct match
  if (EXERCISE_DATABASE[name]) {
    return EXERCISE_DATABASE[name];
  }

  // Search by alternative names
  const nameLower = name.toLowerCase().trim();
  for (const [key, exercise] of Object.entries(EXERCISE_DATABASE)) {
    if (key.toLowerCase() === nameLower) {
      return exercise;
    }
    if (exercise.alternativeNames.some(alt => alt.toLowerCase() === nameLower)) {
      return exercise;
    }
  }

  // Partial match
  for (const [key, exercise] of Object.entries(EXERCISE_DATABASE)) {
    if (key.toLowerCase().includes(nameLower) || nameLower.includes(key.toLowerCase())) {
      return exercise;
    }
  }

  return null;
}

// Extract exercise names from workout description
export function extractExercisesFromDescription(description: string): string[] {
  const exercises: string[] = [];
  const lines = description.split('\n');
  
  for (const line of lines) {
    // Pattern: "1. Exercise Name - 3x12" or "Exercise Name - 3x10-12"
    const match = line.match(/^\d+\.\s*([^-–]+)/);
    if (match) {
      const exerciseName = match[1].trim();
      if (exerciseName && findExercise(exerciseName)) {
        exercises.push(exerciseName);
      }
    }
  }
  
  return exercises;
}
